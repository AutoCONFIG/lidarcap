#!/bin/bash

set -e

# ============================================================================
# LidarCap 训练脚本 - 从配置文件读取所有参数
# ============================================================================

echo "=========================================="
echo "LidarCap 训练启动"
echo "=========================================="
echo ""
echo "配置文件: config/"
echo "  - train.yaml   (训练超参数 + 模型配置)"
echo "  - runtime.yaml (运行时配置 + 数据集配置)"
echo ""

# 执行训练（所有参数从配置文件读取）
python train.py --config-dir config

EXIT_CODE=$?

echo ""
echo "=========================================="
if [ $EXIT_CODE -eq 0 ]; then
    echo "训练完成!"
else
    echo "训练失败，退出码: $EXIT_CODE"
fi
echo "=========================================="

exit $EXIT_CODE
