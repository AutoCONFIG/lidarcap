#!/bin/bash

set -e

# ============================================================================
# LidarCap 训练脚本
# ============================================================================
# 说明：此脚本用于启动训练，训练策略参数可在 base.yaml 中配置
# 也可以通过命令行参数覆盖配置文件中的设置
# ============================================================================
# 
# 使用方法：
# 1. 基本训练（使用 base.yaml 中的默认配置）
#    bash train.sh
#
# 2. 覆盖配置参数
#    bash train.sh --gpu 0 --bs 16
#
# 3. 恢复训练
#    修改下方的 RESUME_PATH，然后运行
#
# 4. 评估/可视化
#    修改下方的 EVAL_MODE 和相关参数，然后运行
# ============================================================================

# ============================================================================
# 可选：训练模式配置（取消注释以启用）
# ============================================================================

# ==================== 恢复训练 ====================
# 取消注释下方这行来恢复之前的训练
# RESUME_PATH="output/run_202511071055"

# ==================== 评估模式 ====================
# 取消注释下方这些行来进行模型评估
# EVAL_MODE=true
# VISUAL=false              # 是否生成可视化结果
# CKPT_PATH=""             # 要评估的检查点路径

# ============================================================================
# 基本配置
# ============================================================================

# 数据集名称（必需）
DATASET="lidarcap"

# ============================================================================
# 构建训练命令
# ============================================================================

CMD="python train.py"

# 添加数据集参数
CMD="$CMD --dataset $DATASET"

# 添加恢复训练参数（如果设置了）
if [ -n "$RESUME_PATH" ]; then
    CMD="$CMD --resume $RESUME_PATH"
fi

# 添加评估模式参数（如果设置了）
if [ "$EVAL_MODE" = true ]; then
    CMD="$CMD --eval"
    if [ "$VISUAL" = true ]; then
        CMD="$CMD --visual"
    fi
    if [ -n "$CKPT_PATH" ]; then
        CMD="$CMD --ckpt_path $CKPT_PATH"
    fi
fi

# 如果提供了其他命令行参数，添加到命令末尾
# 这允许覆盖 base.yaml 中的配置
for arg in "$@"; do
    CMD="$CMD $arg"
done

# ============================================================================
# 执行训练
# ============================================================================

echo "=========================================="
echo "LidarCap 训练脚本"
echo "=========================================="
echo ""
echo "命令："
echo "$CMD"
echo ""
echo "=========================================="
echo ""

# 执行命令
eval $CMD

echo ""
echo "=========================================="
echo "训练完成！"
echo "=========================================="
